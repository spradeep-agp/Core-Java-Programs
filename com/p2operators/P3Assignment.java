package com.p2operators;

public class P3Assignment {

	public static void main(String[] args) {
		
		int a = 5;
		
		a += 3; //a = a + 3;
		System.out.println(a);
		
		a -= 3; //a = a - 3;
		System.out.println(a);
		
		a *= 3; //a = a * 3;
		System.out.println(a);
		
		a /= 3; //a = a / 3;
		System.out.println(a);
		
		a %= 3; //a = a % 3;
		System.out.println(a);		
	}
}
