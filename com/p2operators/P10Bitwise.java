package com.p2operators;

public class P10Bitwise {

	//Bitwise Operators
	/*
	 	& 	-> AND
	 	| 	-> OR
	 	~ 	-> One's Complement
	 	^ 	-> XOR
	 	<< 	-> Left Shift
	 	>> 	-> Right Shift	
	 */
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = 12;
		
		System.out.println(a & b); //8
		/*
		 * 	a = 10	=> 0000 1010
		 *  b = 12 	=> 0000 1100
		 *  ----------------------
		 *  a & b	=> 0000 1000 => 8
		 *  ----------------------
		 */
		
		System.out.println(a | b); //14
		/*
		 * 	a = 10	=> 0000 1010
		 *  b = 12 	=> 0000 1100
		 *  ----------------------
		 *  a | b	=> 0000 1110 => 14
		 *  ----------------------
		 */
		
		System.out.println(a ^ b); //6
		/*
		 * 	a = 10	=> 0000 1010
		 *  b = 12 	=> 0000 1100
		 *  ----------------------
		 *  a ^ b	=> 0000 0110 => 6
		 *  ----------------------
		 */
		
		System.out.println(~a); //-11
		/*
		 * 	a = 10	=> 0000 1010
		 *  ----------------------
		 * 	~a		=> 1111 0101 => -11
		 *  ----------------------
		 */
		
		System.out.println(a<<2); //48
		/*
		 * 	a = 10	=> 0000 1010
		 *  ----------------------
		 * 	a << 2	=> 0010 1000 => 40
		 *  ----------------------
		 */
		
		System.out.println(a>>2); //2
		/*
		 * 	a = 10	=> 0000 1010
		 *  ----------------------
		 * 	a >> 2	=> 0000 0010 => 2
		 *  ----------------------
		 */

	}

}
