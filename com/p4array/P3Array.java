package com.p4array;

public class P3Array
{
	public static void main(String r[])
	{

		//Declaring & Defining the array
		String[] name = new String[5];
		
		//Assigning values to the arrays
		name[0] = "Raj";
		name[1] = "Mani";
		name[2] = "Bala";
		name[3] = "Viji";
		name[4] = "Mahesh";		

		//Displaying the array values
		for(String abc: name)
			System.out.println(abc);

	}
}
		